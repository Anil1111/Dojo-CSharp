using System;

class Calculations
{
	static void Main()
	{
		int num1 = 34;
		int num2 = 53;
		
		Console.WriteLine(num1 + " + " + num2 + " = " + (num1 + num2));
		Console.WriteLine(num1 + " - " + num2 + " = " + (num1 - num2));
		Console.WriteLine(num1 + " * " + num2 + " = " + (num1 * num2));
		Console.WriteLine(num1 + " / " + num2 + " = " + (num1 / num2));
		Console.WriteLine(num1 + " % " + num2 + " = " + (num1 % num2));
	}
}